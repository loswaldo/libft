#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t i;
	size_t c;

	i = 0;
	c = 0;
	if (!src || !dst)
		return (0);
	while (src[c])
		c++;
	if (dstsize == 0)
		return (c);
	while (i < dstsize - 1 && src[i] != 0)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = 0;
	return (c);
}
